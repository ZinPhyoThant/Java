package Java.daily.day1.controlflow;

public class ForLoop {
    public static void main(String[] args) {

        //For Loop
        System.out.println("This is For Loop");
        for (int i=1;i<10;i++){
            System.out.print(i+ "\t");
        }
        System.out.println();

        //Decrement
        System.out.println("Decrement");
        for (int i=10;i>0;i--){
            System.out.print(i+"\t");
        }
        System.out.println();

        //

    }
}
